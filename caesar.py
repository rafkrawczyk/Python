        best_word_count = 0
        best_message = self.message_text

        for shift in range(26):
            # Try this shift
            decrypted = self.apply_shift(shift)
            
            # Split into words and count valid ones
            words = decrypted.split(' ')
            valid_count = 0
            
            for word in words:
                if is_word(self.valid_words, word):
                    valid_count += 1
            
